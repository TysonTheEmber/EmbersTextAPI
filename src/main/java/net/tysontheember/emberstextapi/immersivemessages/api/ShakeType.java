package net.tysontheember.emberstextapi.immersivemessages.api;

/**
 * Types of shaking animations available for {@link ImmersiveMessage}.
 */
public enum ShakeType {
    /** Sinusoidal vertical wave. */
    WAVE,
    /** Circular motion around the origin. */
    CIRCLE,
    /** Random jitter each frame. */
    RANDOM;
}
